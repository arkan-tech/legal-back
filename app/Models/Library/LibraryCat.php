<?php

namespace App\Models\Library;

use App\Models\Library\Books\Book;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\SoftDeletes;

class LibraryCat extends Model
{
    use HasFactory, SoftDeletes;

    protected $table = 'librarycats';

    protected $fillable = [
        'title', 'image', 'parent', 'created_at', 'updated_at','description'
    ];


    public function getImageAttribute()
    {
        return !empty($this->attributes['image']) || !is_null($this->attributes['image']) ? asset('uploads/library/' . str_replace('\\', '/', $this->attributes['image'])) : asset('uploads/person.png');

    }

    protected static function boot()
    {
        parent::boot(); // TODO: Change the autogenerated stub
        self::deleted(function ($model) {
            LibraryCat::where('parent',$model->id)->delete();
            Book::where('CatID',$model->id)->delete();
        });
    }
}
