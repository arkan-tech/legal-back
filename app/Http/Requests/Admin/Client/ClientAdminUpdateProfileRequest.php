            'type.required_if' => 'حقل الصفة مطلوب',
            'phone_code.required_if' => 'الحقل مقدمة الدولة مطلوب',
            'region.required_if' => 'حقل المنطقة مطلوب',
            'city.required_if' => 'حقل المدينة مطلوب',

            'longitude.required_if' => 'الحقل مطلوب',
            'latitude.required_if' => 'الحقل مطلوب',
            'password.required_if' => 'الحقل مطلوب',

            'email.required_if' => 'الحقل الايميل مطلوب ',
            'email.email' => 'يرجى ادخال ايميل صحيح   ',
            'email.unique' => 'الايميل موجود بالفعل ',

            'phone.required_if' => 'الحقل رقم الجوال مطلوب ',
            'phone.numeric' => 'يرجى ادخال رقم جوال صحيح   ',
